/** Get events active in a given month (1-12) */
export function getEventsForMonth(month: number): BayEvent[] {
  return allBayEvents.filter(e => e.months.includes(month));
}

/** Get events that create restricted zones or major traffic */
export function getHighImpactEvents(): BayEvent[] {
  return allBayEvents.filter(e => e.restrictedZone === 'major' || e.restrictedZone === 'moderate');
}

/** Get events by category */
export function getEventsByCategory(category: EventCategory): BayEvent[] {
  return allBayEvents.filter(e => e.category === category);
}

/** Get events for a specific day of week (0=Sun, 6=Sat) */
export function getRecurringEventsForDay(dayOfWeek: number): BayEvent[] {
  return allBayEvents.filter(e => e.dayOfWeek === dayOfWeek);
}

