// San Francisco Bay recurring boating events, regattas, parades, and marine gatherings.
// Last researched: 2026-04-09. Update annually each January.
// Sources: YRA, PICYA, Latitude 38, individual yacht club calendars, USCG LNMs.

export interface BayEvent {
  id: string;
  name: string;
  category: EventCategory;
  organizer: string;
  /** Cron-like month range or specific month, e.g. "9" for September, "4-9" for Apr-Sep */
  months: number[];
  /** Human-readable schedule description */
  schedule: string;
  /** Day of week if recurring weeknight/weekend (0=Sun, 1=Mon, ... 6=Sat) */
  dayOfWeek?: number;
  /** General Bay area */
  location: string;
  /** Approximate lat/lng for map placement */
  lat?: number;
  lng?: number;
  /** Rough number of participating boats or spectators */
  sizeEstimate: string;
  url?: string;
  /** Does this event create USCG restricted zones or traffic advisories? */
  restrictedZone: 'major' | 'moderate' | 'minor' | 'none';
  /** Brief description of traffic impact for other boaters */
  trafficNote?: string;
  /** For biennial events: 'even' or 'odd' years only */
  biennial?: 'even' | 'odd';
  /** Approximate date for the current year (YYYY-MM-DD). Null for recurring weekly events. */
  typicalDate?: string;
  /** End date if multi-day event */
  typicalEndDate?: string;
}

export type EventCategory =
  | 'major-regatta'
  | 'club-regatta'
  | 'one-design'
  | 'midwinter'
  | 'beer-can'
  | 'offshore'
  | 'foil-race'
  | 'boat-show'
  | 'parade'
  | 'fleet-week'
  | 'lighted-parade'
  | 'holiday'
  | 'swim'
  | 'paddle'
  | 'dragon-boat'
  | 'fishing'
  | 'heritage'
  | 'youth'
  | 'community';

